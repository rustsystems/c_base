#ifndef UXAS_STDUXAS_H
#define	UXAS_STDUXAS_H

#include <memory>

namespace uxas
{
namespace stduxas
{
template<typename T, typename... Args>
std::unique_ptr<T> make_unique(Args&&... args)
{
    return std::unique_ptr<T>(new T(std::forward<Args>(args)...));
}
}; //namespace stduxas
}; //namespace uxas

#endif	/* UXAS_STDUXAS_H */
